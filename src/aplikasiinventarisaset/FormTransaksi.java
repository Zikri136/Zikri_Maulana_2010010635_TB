    public void tampilan(){
        Object [] baris = {"No", "ID Transaksi", "Aset",  "Tanggal Transaksi", "Keterangan"};
        tabMode = new DefaultTableModel(null, baris);
        tableTransaksi.setModel(tabMode);
        try {
                String sql = "Select * From daftar_transaksi_aset";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String id = rs.getString("id_transaksi");
                        String nk = rs.getString("id_aset");
                        String tk = rs.getString("tanggal_transaksi");
                        String dp = rs.getString("keterangan_transksi");
                        Object [] data = {no,id,nk,tk, dp};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }    
    public void setAset(){
        try {
            String sql = "SELECT * FROM t_aset";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()){              
                cmbAset.addItem(rs.getString("nama_aset"));
            }
            
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
     
    
