    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String  date = "yyyy-MM-dd";
        SimpleDateFormat dt = new SimpleDateFormat(date);
        
        String id_aset = cmbAset.getSelectedItem().toString();
        String tgl_transaksi = String.valueOf(dt.format(tglTr.getDate()));
        
        
        if (txtID.getText().equals("")|| tglTr.getDate().equals("") || txtKet.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!!");
        } else {
            try {
               int s;
               String sql = "INSERT INTO daftar_transaksi_aset VALUES ('"+txtID.getText()+"', '"+id_aset+"' , '"+tgl_transaksi+"'  , '"+txtKet.getText()+"')";
               st = conn.createStatement();
               s = st.executeUpdate(sql);
               if (s == 1){
                JOptionPane.showMessageDialog(null, "Berhasil disimpan");
                clear();
                tampilan();
               }
           } catch (Exception e){
                System.out.println(e.toString());
           }    
        }   
    }//GEN-LAST:event_btnSimpanActionPerformed

