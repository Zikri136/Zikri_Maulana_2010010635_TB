    public void tampilan(){
        Object [] baris = {"No", "ID Kategori", "Nama", "Deskripsi"};
        tabMode = new DefaultTableModel(null, baris);
        tablePengguna.setModel(tabMode);
        try {
                String sql = "SELECT * FROM t_kategori_aset";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String id = rs.getString("id_kategori");
                        String nk = rs.getString("nama_kategori");
                        String tk = rs.getString("deskripsi_kategori");
                        

                        Object [] data = {no,id,nk,tk};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    
